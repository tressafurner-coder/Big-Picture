/**
 * Writes src/app/generated/prototypeHubDates.ts with display timestamps for the hub table.
 * Uses the latest commit touching each prototype's source paths (`git log`), otherwise
 * falls back to the newest file mtime among those paths.
 */
import { execSync } from "node:child_process";
import { existsSync, mkdirSync, statSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const outDir = join(root, "src/app/generated");
const outFile = join(outDir, "prototypeHubDates.ts");

const MONTHS_SHORT = [
  "Jan",
  "Feb",
  "Mar",
  "Apr",
  "May",
  "Jun",
  "Jul",
  "Aug",
  "Sep",
  "Oct",
  "Nov",
  "Dec",
];

const PROTOTYPES = {
  TEAMS_PROTOTYPE_LAST_EDITED_DISPLAY: [
    "src/app/TeamsDemoPage.tsx",
    "src/app/components/BigPictureTeamsDropdown.tsx",
    "src/app/components/BigPictureTag.tsx",
    "src/app/components/GlobalTeamsSplash.tsx",
  ],
  CHATBOT_LAST_UPDATE_DISPLAY: [
    "src/app/ChatbotPrototype.tsx",
    "src/app/components/ChatOverlay.tsx",
    "src/app/components/ChatButton.tsx",
  ],
  RISK_MANAGEMENT_JIRA_MAPPING_LAST_UPDATE_DISPLAY: [
    "src/app/RiskManagementJiraMappingPage.tsx",
  ],
  TEST_PROTOTYPE_LAST_UPDATE_DISPLAY: ["src/app/TestPrototypePage.tsx"],
  MERGING_BOARD_GOALS_LAST_UPDATE_DISPLAY: ["src/app/MergingBoardGoalsPage.tsx"],
};

function formatHubStamp(isoOrDate) {
  const d =
    typeof isoOrDate === "string" ? new Date(isoOrDate) : isoOrDate;
  if (Number.isNaN(d.getTime())) return "—";
  const day = d.getDate();
  const mon = MONTHS_SHORT[d.getMonth()];
  const y = d.getFullYear();
  const hh = String(d.getHours()).padStart(2, "0");
  const mm = String(d.getMinutes()).padStart(2, "0");
  return `${day} ${mon} ${y}, ${hh}:${mm}`;
}

function gitLatestCommitIso(paths) {
  try {
    const rel = paths.join(" ");
    const cmd = `git log -1 --format=%cI -- ${rel}`;
    const out = execSync(cmd, {
      cwd: root,
      encoding: "utf8",
      stdio: ["pipe", "pipe", "pipe"],
    }).trim();
    return out || null;
  } catch {
    return null;
  }
}

function fallbackMtimeIso(paths) {
  let latestMs = 0;
  for (const rel of paths) {
    const fp = join(root, rel);
    if (existsSync(fp)) {
      const t = statSync(fp).mtimeMs;
      if (t > latestMs) latestMs = t;
    }
  }
  if (!latestMs) return new Date().toISOString();
  return new Date(latestMs).toISOString();
}

function stampForPaths(paths) {
  const gitIso = gitLatestCommitIso(paths);
  if (gitIso) return formatHubStamp(gitIso);
  return formatHubStamp(fallbackMtimeIso(paths));
}

const stamps = {};
for (const [exportName, paths] of Object.entries(PROTOTYPES)) {
  stamps[exportName] = stampForPaths(paths);
}

const body = `/* eslint-disable -- generated by scripts/generate-prototype-hub-dates.mjs */
/**
 * Hub “Last updated” values — regenerated on predev/prebuild from git history (or file mtimes).
 * Do not edit by hand; change prototype files and run dev/build.
 */
export const TEAMS_PROTOTYPE_LAST_EDITED_DISPLAY = ${JSON.stringify(stamps.TEAMS_PROTOTYPE_LAST_EDITED_DISPLAY)};
export const CHATBOT_LAST_UPDATE_DISPLAY = ${JSON.stringify(stamps.CHATBOT_LAST_UPDATE_DISPLAY)};
export const RISK_MANAGEMENT_JIRA_MAPPING_LAST_UPDATE_DISPLAY = ${JSON.stringify(stamps.RISK_MANAGEMENT_JIRA_MAPPING_LAST_UPDATE_DISPLAY)};
export const TEST_PROTOTYPE_LAST_UPDATE_DISPLAY = ${JSON.stringify(stamps.TEST_PROTOTYPE_LAST_UPDATE_DISPLAY)};
export const MERGING_BOARD_GOALS_LAST_UPDATE_DISPLAY = ${JSON.stringify(stamps.MERGING_BOARD_GOALS_LAST_UPDATE_DISPLAY)};
`;

mkdirSync(outDir, { recursive: true });
writeFileSync(outFile, body, "utf8");
console.log(
  `generate-prototype-hub-dates: wrote ${stamps.TEAMS_PROTOTYPE_LAST_EDITED_DISPLAY} (teams), ${stamps.CHATBOT_LAST_UPDATE_DISPLAY} (chatbot), ${stamps.RISK_MANAGEMENT_JIRA_MAPPING_LAST_UPDATE_DISPLAY} (risk jira), ${stamps.TEST_PROTOTYPE_LAST_UPDATE_DISPLAY} (test), ${stamps.MERGING_BOARD_GOALS_LAST_UPDATE_DISPLAY} (merging board goals) → src/app/generated/prototypeHubDates.ts`,
);
